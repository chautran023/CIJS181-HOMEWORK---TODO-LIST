export function WordCount(){
  const [wordCount, setWordCount] = useState(0);
  const [inputChange, setInputChange] = useState('');
  useEffect(() => {
      const wordsArray = inputChange.match(/\b\w+\b/g);
      const currentWordCount = wordsArray ? wordsArray.length : 0;
      const timeoutId = setTimeout(() => {setWordCount(currentWordCount)}, 500);
      return () => clearTimeout(timeoutId); // sau khi đếm hết mới setWordCount thay vì mỗi lần đếm là 1 lần setWordCount.
      }, [inputChange]);
  
      return (
          <div className="mb-3">
              <div>
              <h3>Bài 2: Đếm từ</h3>
                  Thực hiện viết ứng dụng đếm chữ với giao diện đơn giản và yêu cầu như sau:<br/>
                  - Hiển thị số từ bên trong `textarea`.<br/>
                  - Số từ được cập nhật sau mỗi 500ms kể từ thời điểm người dùng ngừng gõ phím.<br/>
              </div>
          <label for="exampleFormControlTextarea1" className="form-label">Textarea</label>
          <textarea className="form-control" id="input-textarea" rows="3"
          onChange = {(e)=>setInputChange(e.target.value)}
          ></textarea>
          <div>Word(s) count: {wordCount}</div>
          </div>
          
      )
}

